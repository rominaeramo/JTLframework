package JTLMM.resource.JTLMM.ui;

public class JTLMMHoverTextProvider implements JTLMM.resource.JTLMM.IJTLMMHoverTextProvider {
	
	private JTLMM.resource.JTLMM.ui.JTLMMDefaultHoverTextProvider defaultProvider = new JTLMM.resource.JTLMM.ui.JTLMMDefaultHoverTextProvider();
	
	public String getHoverText(org.eclipse.emf.ecore.EObject container, org.eclipse.emf.ecore.EObject referencedObject) {
		// Set option overrideHoverTextProvider to false and customize this method to
		// obtain custom hover texts.
		return defaultProvider.getHoverText(referencedObject);
	}
	
	public String getHoverText(org.eclipse.emf.ecore.EObject object) {
		// Set option overrideHoverTextProvider to false and customize this method to
		// obtain custom hover texts.
		return defaultProvider.getHoverText(object);
	}
	
}
